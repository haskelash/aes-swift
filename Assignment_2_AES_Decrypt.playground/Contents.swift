let roundConstants: [UInt8] = [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]

func AESDecrypt(inputs: [DecryptionInput]) -> [String] {
  return inputs
    .map{ (stringToBytes($0.key), stringToBytes($0.ciphertext), $0.mode) }
    .map{ AESDecrypt(input: $0) }
    .map{ bytes in
      if let string = String(bytes: bytes, encoding: .ascii) {
        return string
      } else {
        fatalError("\(bytes) is not ASCII encoded.")
      }
    }
}

func stringToBytes(_ string: String) -> [UInt8] {
  let length = string.count
  if length % 2 == 1 {
    fatalError("""
      Hex string "\(string)" of odd length \(length) can't be converted to byte array.
      """)
  }
  var bytes = [UInt8]()
  bytes.reserveCapacity(length/2)
  var index = string.startIndex
  for _ in 0 ..< length/2 {
    let nextIndex = string.index(index, offsetBy: 2)
    if let byte = UInt8(string[index ..< nextIndex], radix: 16) {
      bytes.append(byte)
    } else {
      fatalError("""
        Hex string "\(string)" contains invalid characters "\(string[index ..< nextIndex])".
        """)
    }
    index = nextIndex
  }
  return bytes
}

func AESDecrypt(input: (key: [UInt8],
                        nonceAndCiphertext: [UInt8],
                        mode: OperationMode)) -> [UInt8] {
  let (nonce, ciphertext) = parseNonceAndCiphertext(input.nonceAndCiphertext)
  switch input.mode {
  case .CBC:
    return AESCBCDecrypt(key: input.key, nonce: nonce, ciphertext: ciphertext)
  case .CTR:
    return AESCTRDecrypt(key: input.key, nonce: nonce, ciphertext: ciphertext)
  }
}

func parseNonceAndCiphertext(_ nonceAndCiphertext: [UInt8]) -> ([UInt8], [UInt8]) {
  let count = nonceAndCiphertext.count
  if count < 17 {
    fatalError("""
      Ciphertext \(nonceAndCiphertext) must have at least 17 bytes (16 for nonce and 1 for
      ciphertext) but has only \(count).
      """)
  }
  return (Array(nonceAndCiphertext[0 ..< 16]), Array(nonceAndCiphertext[16 ..< count]))
}

func AESCBCDecrypt(key: [UInt8], nonce: [UInt8], ciphertext: [UInt8]) -> [UInt8] {
  if ciphertext.count % 16 != 0 {
    fatalError("""
      Ciphertext \(ciphertext) must have a multiple of 16 bytes but has \(ciphertext.count).
      """)
  }
  var plaintext = [UInt8]()
  let keys = roundKeys(from: key)
  for i in 0 ..< ciphertext.count/16 {
    let block = Array(ciphertext[i*16 ..< (i+1)*16])
    let decrypted = AESSingleBlockDecrypt(keys: keys, ciphertext: block)
    let pad = i == 0 ? nonce : Array(ciphertext[(i-1)*16 ..< i*16])
    plaintext.append(contentsOf: zip(decrypted, pad).map{ $0 ^ $1 })
  }
  return plaintext
}

func AESCTRDecrypt(key: [UInt8], nonce: [UInt8], ciphertext: [UInt8]) -> [UInt8] {
  var plaintext = [UInt8]()
  let keys = roundKeys(from: key)
  for i in 0 ..< Int(ceil(Double(ciphertext.count)/16.0)) {
    var counter = nonce
    /*
      Note: this line assumes there are < 256 blocks, and so it always increments the last byte.
      If you have at least 256 blocks, you need to string together the 16-byte nonce array into a
      128 bit integer then add the block number, which will affect more than just the last byte.
    */
    counter[15] += UInt8(i)
    let pad = AESSingleBlockEncrypt(keys: keys, ciphertext: counter)
    let block = Array(ciphertext[i*16 ..< min((i+1)*16, ciphertext.count)])
    plaintext.append(contentsOf: zip(block, pad).map{ $0 ^ $1 })
  }
  return plaintext
}

func AESSingleBlockEncrypt(keys: [[UInt8]], ciphertext: [UInt8]) -> [UInt8] {
  var plaintext = ciphertext
  plaintext = zip(plaintext, keys[0]).map{ $0 ^ $1 }
  for i in (1 ..< 11) {
    plaintext = substituteWord(plaintext)
    plaintext = shiftRows(plaintext)
    if i != 10 {
      plaintext = mixColumns(plaintext)
    }
    plaintext = zip(plaintext, keys[i]).map{ $0 ^ $1 }
  }
  return plaintext
}

func AESSingleBlockDecrypt(keys: [[UInt8]], ciphertext: [UInt8]) -> [UInt8] {
  var plaintext = ciphertext
  for i in (1 ..< 11).reversed() {
    plaintext = zip(plaintext, keys[i]).map{ $0 ^ $1 }
    if i != 10 {
      plaintext = mixColumnsInverse(plaintext)
    }
    plaintext = shiftRowsInverse(plaintext)
    plaintext = substituteWordInverse(plaintext)
  }
  plaintext = zip(plaintext, keys[0]).map{ $0 ^ $1 }
  return plaintext
}

func roundKeys(from key: [UInt8]) -> [[UInt8]] {
  var keys = [key.enumerated().reduce([[UInt8]]()) { words, iterator in
    var words = words
    if iterator.offset % 4 == 0 {
      words.append([iterator.element])
    } else {
      words[iterator.offset/4].append(iterator.element)
    }
    return words
  }]

  for round in 1 ..< 11 {
    var words = [[UInt8]]()
    for i in 0 ..< 4 {
      if i == 0 {
        let subbedRotatedLast = substituteWord(rotateWord(keys[round-1][3]))
        words.append(
          zip(keys[round-1][0], subbedRotatedLast)
          .map{ $0 ^ $1 }
          .enumerated()
          .map{ $0.offset == 0 ? $0.element ^ roundConstants[round-1] : $0.element })


      } else {
        words.append(zip(words[i-1], keys[round-1][i]).map{ $0 ^ $1 })
      }
    }
    keys.append(words)
  }
  return keys.map{ $0.flatMap{ $0 } }
}

func substituteWord(_ word: [UInt8]) -> [UInt8] {
  return word.map{ sBox[Int($0)] }
}

func substituteWordInverse(_ word: [UInt8]) -> [UInt8] {
  return word.map{ sBoxInverse[Int($0)] }
}

func rotateWord(_ word: [UInt8]) -> [UInt8] {
  return [word[1], word[2], word[3], word[0]]
}

func shiftRows(_ rows: [UInt8]) -> [UInt8] {
  var shifted = [UInt8](repeating: 0, count: 16)
  for row in 0 ..< 4 {
    for col in 0 ..< 4 {
      let copyCol = (col+row) % 4
      shifted[col*4 + row] = rows[copyCol*4 + row]
    }
  }
  return shifted
}

func shiftRowsInverse(_ rows: [UInt8]) -> [UInt8] {
  var shifted = [UInt8](repeating: 0, count: 16)
  for row in 0 ..< 4 {
    for col in 0 ..< 4 {
      let copyCol = (col-row+4) % 4
      shifted[col*4 + row] = rows[copyCol*4 + row]
    }
  }
  return shifted
}

func mixColumns(_ columns: [UInt8]) -> [UInt8] {
  var mixed = [[UInt8]](repeating: [UInt8](repeating: 0, count: 4), count: 4)
  for row in 0 ..< 4 {
    for col in 0 ..< 4 {
      let a = mixColumnTransform[row][0][Int(columns[col*4 + 0])]
      let b = mixColumnTransform[row][1][Int(columns[col*4 + 1])]
      let c = mixColumnTransform[row][2][Int(columns[col*4 + 2])]
      let d = mixColumnTransform[row][3][Int(columns[col*4 + 3])]
      mixed[col][row] = a ^ b ^ c ^ d
    }
  }
  return mixed.flatMap{ $0 }
}

func mixColumnsInverse(_ columns: [UInt8]) -> [UInt8] {
  var mixed = [[UInt8]](repeating: [UInt8](repeating: 0, count: 4), count: 4)
  for row in 0 ..< 4 {
    for col in 0 ..< 4 {
      let a = inverseMixColumnTransform[row][0][Int(columns[col*4 + 0])]
      let b = inverseMixColumnTransform[row][1][Int(columns[col*4 + 1])]
      let c = inverseMixColumnTransform[row][2][Int(columns[col*4 + 2])]
      let d = inverseMixColumnTransform[row][3][Int(columns[col*4 + 3])]
      mixed[col][row] = a ^ b ^ c ^ d
    }
  }
  return mixed.flatMap{ $0 }
}

